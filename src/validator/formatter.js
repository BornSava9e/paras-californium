
    function clearWhiteSpace(text){
        return text.trim(); // trim() method clear all the white space present in the string.
    }

    function lwrCase(string){
        return string.toLowerCase(); // toLowerCase() method will change the string to lowercase character.
    }

    function uprCase(string){
        return string.toUpperCase(); // toUpperCase() method will change the string to upper case characters.
    }

    module.exports.clearWhiteSpace = clearWhiteSpace
    module.exports.lwrCase = lwrCase 
    module.exports.uprCase = uprCase



